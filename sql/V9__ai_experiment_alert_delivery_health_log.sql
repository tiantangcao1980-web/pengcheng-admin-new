CREATE TABLE IF NOT EXISTS `ai_experiment_alert_delivery_health_log` (
  `id` bigint NOT NULL AUTO_INCREMENT COMMENT '主键',
  `range_days` int NOT NULL COMMENT '统计窗口天数',
  `health_level` varchar(16) NOT NULL COMMENT '健康级别(HEALTHY/WARNING/CRITICAL)',
  `previous_health_level` varchar(16) DEFAULT NULL COMMENT '上一健康级别',
  `level_changed` tinyint NOT NULL DEFAULT 0 COMMENT '是否发生级别变化(0/1)',
  `escalated` tinyint NOT NULL DEFAULT 0 COMMENT '是否触发升级告警(0/1)',
  `suppressed` tinyint NOT NULL DEFAULT 0 COMMENT '是否命中冷却抑制(0/1)',
  `warning_notified` tinyint NOT NULL DEFAULT 0 COMMENT '是否触发预警通知(0/1)',
  `recovery_notified` tinyint NOT NULL DEFAULT 0 COMMENT '是否触发恢复通知(0/1)',
  `reason` varchar(64) DEFAULT NULL COMMENT '处理原因标记',
  `dead_rate` decimal(10,6) NOT NULL COMMENT '死信率',
  `pending_rate` decimal(10,6) NOT NULL COMMENT '待重试率',
  `dead_rate_threshold` decimal(10,6) NOT NULL COMMENT '死信率阈值',
  `pending_rate_threshold` decimal(10,6) NOT NULL COMMENT '待重试率阈值',
  `total_count` bigint NOT NULL DEFAULT 0 COMMENT '总投递数',
  `success_count` bigint NOT NULL DEFAULT 0 COMMENT '成功数',
  `pending_count` bigint NOT NULL DEFAULT 0 COMMENT '待重试数',
  `dead_count` bigint NOT NULL DEFAULT 0 COMMENT '死信数',
  `closed_count` bigint NOT NULL DEFAULT 0 COMMENT '已关闭数',
  `failed_count` bigint NOT NULL DEFAULT 0 COMMENT '失败(旧状态)数',
  `suggestion` varchar(255) DEFAULT NULL COMMENT '处置建议',
  `check_source` varchar(16) NOT NULL DEFAULT 'scheduler' COMMENT '巡检来源(manual/scheduler)',
  `check_time` datetime NOT NULL COMMENT '巡检时间',
  `create_time` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  PRIMARY KEY (`id`),
  KEY `idx_check_time` (`check_time`),
  KEY `idx_health_level_time` (`health_level`, `check_time`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_0900_ai_ci COMMENT='AI实验告警投递健康巡检日志';
